/**
 * Public Report Route — Renders a session's markdown report as a
 * standalone HTML page that can be shared without authentication.
 *
 * GET /api/sessions/[sessionId]/public
 */

import { NextResponse } from "next/server";
import { generateSessionExport } from "@/lib/export";

export async function GET(
  _request: Request,
  { params }: { params: Promise<{ sessionId: string }> }
) {
  try {
    const { sessionId } = await params;
    const { markdown } = await generateSessionExport(sessionId);

    // Convert markdown to basic HTML
    const html = renderMarkdownPage(markdown, sessionId);

    return new NextResponse(html, {
      headers: {
        "Content-Type": "text/html; charset=utf-8",
        "Cache-Control": "public, max-age=300",
      },
    });
  } catch {
    return new NextResponse(
      "<html><body><h1>Report not found</h1><p>This review may not exist or hasn't completed yet.</p></body></html>",
      { status: 404, headers: { "Content-Type": "text/html" } }
    );
  }
}

