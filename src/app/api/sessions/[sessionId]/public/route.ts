/** Minimal markdown → HTML converter (no external deps). */
function simpleMarkdownToHtml(md: string): string {
  return md
    .split("\n")
    .map((line) => {
      // Headers
      if (line.startsWith("### ")) return `<h3>${esc(line.slice(4))}</h3>`;
      if (line.startsWith("## ")) return `<h2>${esc(line.slice(3))}</h2>`;
      if (line.startsWith("# ")) return `<h1>${esc(line.slice(2))}</h1>`;
      // Blockquote
      if (line.startsWith("> ")) return `<blockquote>${inline(line.slice(2))}</blockquote>`;
      // Horizontal rule
      if (/^---+$/.test(line.trim())) return "<hr />";
      // List items
      if (/^(\d+)\.\s/.test(line)) return `<li>${inline(line.replace(/^\d+\.\s/, ""))}</li>`;
      if (line.startsWith("- ")) return `<li>${inline(line.slice(2))}</li>`;
      if (line.startsWith("  - ")) return `<li style="margin-left:1rem">${inline(line.slice(4))}</li>`;
      // Empty line
      if (line.trim() === "") return "<br />";
      // Paragraph
      return `<p>${inline(line)}</p>`;
    })
    .join("\n");
}

function inline(text: string): string {
  return esc(text)
    .replace(/\*\*(.+?)\*\*/g, "<strong>$1</strong>")
    .replace(/`(.+?)`/g, "<code>$1</code>")
    .replace(/\[(.+?)\]\((.+?)\)/g, '<a href="$2" target="_blank" rel="noopener">$1</a>');
}

function esc(s: string): string {
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}
